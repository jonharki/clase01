
var numeroA = prompt ('ingrese un valor');
var numeroB = prompt ('ingrese otro valor');
var resultadoA = parseInt(numeroA) + parseInt(numeroB);
var resultadoB = parseInt(numeroA) - parseInt(numeroB);
var resultadoC = parseInt(numeroA) * parseInt(numeroB);
var resultadoD = parseInt(numeroA) / parseInt(numeroB);
alert ('El resultado de la suma es ' +  (resultadoA));
alert ('El resultado de la resta es ' +  (resultadoB));
alert ('El resultado de la multiplicacion es ' +  (resultadoC));
alert ('El resultado de la division es ' +  (resultadoD));
